export class carton {
    constructor() {
        this.b = [];
        this.i = [];
        this.n = [];
        this.g = [];
        this.o = [];
        this.secuencia = [];
    }
    set setB(b) {
        this.b = b;
    }
    get getB() {
        return this.b;
    }
    set setI(b) {
        this.i = b;
    }
    get getI() {
        return this.i;
    }
    set setN(b) {
        this.n = b;
    }
    get getN() {
        return this.n;
    }
    set setG(b) {
        this.g = b;
    }
    get getG() {
        return this.g;
    }
    set setO(b) {
        this.o = b;
    }
    get getO() {
        return this.o;
    }
    set setSecuencia(b) {
        this.secuencia = b;
    }
    get getSecuencia() {
        return this.secuencia;
    }
}
